# 1. "gets" asks the user to input something. "chomp" is a method that removes the black line or \n

print "What is your favorite holiday? "
holiday = gets.chomp
print "What food do you like to eat on that holiday? "
food = gets.chomp
print "Who do you like to be with on this holiday? "
who = gets.chomp

puts "You like to eat #{food} on #{holiday} and be with #{who}."
